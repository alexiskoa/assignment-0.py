def three_number_average(x, y, z):
    '''Item 1.
    Three Number Average. 3 points.
    Returns the average of three numbers.
    Parameters
    ----------
    x: int
        the first number
    y: int
        the second number
    z: int
        the third number
    Returns
    -------
    float
        the average of x, y, and z
    '''
    # Write your code below this line
    avg = (x + y + z)/3
    return avg

